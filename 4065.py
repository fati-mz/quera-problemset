# ورودی‌ها را دریافت می‌کنیم
a, b, l = map(int, input().split())

# زمان شروع
time = 0

# شمارش عرعرها
for i in range(l):
    if i % 2 == 0:
        # اگر عرعر شماره زوج است، `a` ثانیه صبر می‌کند
        time += a
    else:
        # اگر عرعر شماره فرد است، `b` ثانیه صبر می‌کند
        time += b

# چاپ زمان آخرین عرعر
print(time)
